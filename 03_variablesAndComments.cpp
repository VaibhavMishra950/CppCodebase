#include<iostream>
using namespace std;

int main(){
    // This is one line Comment.

    /*
    This 
    is 
    multi
    line
    comment.
    */
    int a = 10;     // Integer Variable
    float b = 5.2f;  // Float Variable
    double c = 5.2;  // Double Variable (Float with high precision)
    char d = 'd';   // Character Variable
    bool e = true;

    cout<<"a: "<<a<<endl;
    cout<<"b: "<<b<<endl;
    cout<<"c: "<<c<<endl;
    cout<<"d: "<<d<<endl;
    cout<<"e: "<<e<<endl;

    return 0;
}